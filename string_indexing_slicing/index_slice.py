## String Indexing

sweet = "rasamalai"
namma_character = sweet[4]
print(namma_character)

## String Slicing

foodie = "I love chocolates"
food = foodie[7:17]
print(food)

## String Slicing without start index

choco_name = "Kitkat"
print(choco_name[:3])

## String Slicing without end index

choco_name = "Kitkat"
print(choco_name[3:])

## String Slicing without both start and end index --> Gives the same string as output

choco_name = "Kitkat"
print(choco_name[:])


## Negative Indexing

choco_name = "Milky Bar"
enaku_venunra_char = choco_name[-3]
print(enaku_venunra_char)

